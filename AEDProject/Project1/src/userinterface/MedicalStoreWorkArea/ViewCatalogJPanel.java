    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRowCount = tblCatalog.getSelectedRowCount();
        if (selectedRowCount <= 0) {
            JOptionPane.showMessageDialog(null, "Please select a row ");
            return;
        }

        int row = tblCatalog.getSelectedRow();

        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure ?", "Warning", JOptionPane.YES_NO_OPTION);
        if (dialogResult == JOptionPane.YES_OPTION) {
            Medicine m = (Medicine) tblCatalog.getValueAt(row, 0);
            for (MedicalStore ms : organization.getMedicalStoreDirectory().getMedicalStoreList()) {
                if (userAccount.getUsername().equalsIgnoreCase(ms.getMedicalStoreName())) {
                    ms.getMedicineList().removeMedicine(m);
                    populateCatalog();
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

