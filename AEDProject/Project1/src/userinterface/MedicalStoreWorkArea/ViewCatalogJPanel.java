    public void populateCatalog() {
        int rowCount = tblCatalog.getRowCount();
        DefaultTableModel model = (DefaultTableModel) tblCatalog.getModel();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        for (MedicalStore ms : organization.getMedicalStoreDirectory().getMedicalStoreList()) {
            if (userAccount.getUsername().equalsIgnoreCase(ms.getMedicalStoreName())) {
                for (Medicine m : ms.getMedicineList().getMedicineList()) {
                    if (m.getAvail() != 0) {
                        Object row[] = new Object[4];
                        row[0] = m;
                        row[1] = m.getPrice();
                        row[2] = m.getAvail();
                        row[3] = m.getExpiryDate();
                        model.addRow(row);
                    }
                }
            }
        }
    }

